export class StarWarsCharacter {
  constructor(id, name, height, mass, hairColor, skinColor, birthYear, email, gender, created, edited) {
    this.id = id;
    this.name = name;
    this.height = height;
    this.mass = mass;
    this.hairColor = hairColor;
    this.skinColor = skinColor;
    this.birthYear = birthYear;
    this.email = email;
    this.gender = gender;
    this.created = created;
    this.edited = edited;
  }
}
